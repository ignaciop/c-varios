#include <iostream>

using namespace std;

int main() {
    int n1 = 0;
    int n2 = 0;

    cout << "Enter first number: ";
    cin >> n1;

    cout << "Enter second number: ";
    cin >> n2;

    cout << n1 << " + " << n2 << " = " << n1 + n2 << "\n";
    cout << n1 << " * " << n2 << " = " << n1 * n2 << "\n";

    return 0;
}