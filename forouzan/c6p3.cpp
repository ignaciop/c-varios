#include <iostream>
#include <cmath>

int main () {
    const double PI = 3.1415F;

    std::cout << sin(0) << std::endl;
    std::cout << sin(PI) << std::endl;
    std::cout << cos(0) << std::endl;
    std::cout << cos(PI) << std::endl;
    std::cout << tan(0) << std::endl;
    std::cout << tan(1) << std::endl;
    std::cout << asin(0) << std::endl;
    std::cout << asin(1) << std::endl;
    std::cout << acos(0) << std::endl;
    std::cout << acos(1) << std::endl;
    std::cout << atan(0) << std::endl;
    std::cout << atan(1) << std::endl;

    return 0;
}